package basics;

public class VariablesAndTypes {
    public static void main(String[] args) {
        String name = "Aziz";
        int yearsExperience = 5;
        boolean refreshingJava = true;
        double targetVersion = 17.0;

        System.out.println("Name: " + name);
        System.out.println("Years of experience: " + yearsExperience);
        System.out.println("Refreshing Java: " + refreshingJava);
        System.out.println("Target Java version: " + targetVersion);
    }
} 
